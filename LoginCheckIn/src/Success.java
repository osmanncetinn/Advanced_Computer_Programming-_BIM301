    private void j_TableDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_j_TableDisplayMouseClicked
        int i = j_TableDisplay.getSelectedRow();
        TableModel model = j_TableDisplay.getModel();
        
        name.setText(model.getValueAt(i, 1).toString());
        address.setText(model.getValueAt(i, 2).toString());
        
        String gender1 = model.getValueAt(i,3).toString();
        if(gender1.equals("Female"))
            female.setSelected(true);
        else
            male.setSelected(true);
        
        String course1 = model.getValueAt(i,4).toString();
        switch(course1){
            case "Java":
                Java.setSelected(true);
                C.setSelected(false);
                break;
            case "C":
                 Java.setSelected(false);
                C.setSelected(true);
                break;
            default:
                 Java.setSelected(true);
                C.setSelected(true);
        }
        
        String subject1 = model.getValueAt(i,5).toString();
        
        switch(subject1){
            case "Computer Engineering":
                subject.setSelectedIndex(0);
                break;
            case "Management":
                subject.setSelectedIndex(1);
                break;
            case " Arts":
               subject.setSelectedIndex(2);
               break;
            case "Electric-Electronic Engineering":
                subject.setSelectedIndex(3);
                break;
        }
    }//GEN-LAST:event_j_TableDisplayMouseClicked

