    public ArrayList<User> userList()
    {
       ArrayList<User> userList = new ArrayList<>();
       try
        {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");//internetten arama motoruna "java sql server connection string"
            String url = "jdbc:sqlserver://localhost : 1433;databaseName = testdb; user = sa; password = Halic123";
            Connection con = DriverManager.getConnection(url);
            String query1 = "select * from users";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            User user;
            
            while(rs.next())
            {
                user = new User(rs.getInt("uid"),rs.getString("name"), rs.getString("address"), rs.getString("gender"), rs.getString("course"), rs.getString("subject"));
                userList.add(user);
            }
        }
       catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
       return userList;
    }
    public void show_user()
    {
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel)j_TableDisplay.getModel();
        Object[] row = new Object[6];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getuid();
            row[1] = list.get(i).getname();
            row[2] = list.get(i).getaddress();
            row[3] = list.get(i).getgender();
            row[4] = list.get(i).getcourse();
            row[5] = list.get(i).getsubject();
            model.addRow(row);
        }
    }
    
    
